    $min = INF;
    $ret = null;
    for($i = 0 ; $i < count($nums); $i++){
        $j = 0;
        $k = count($nums) - 1;
        while($j < $i && $k > $i){
            $s = $nums[$i] + $nums[$j] + $nums[$k];
            $n = abs($target - $s);
            if($n == 0) return $s;
            if($min > $n) {$min = $n; $ret = $s;}
            if($s > $target)$k--;
            else $j++;
        }
    }
    return $ret;
}

